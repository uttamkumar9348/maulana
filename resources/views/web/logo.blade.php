                        <h4 class="inner-title">Concept note on Charkha <img src="{{asset('web/images/chakra.png')}}" alt="chakra image"/></h4>
                        <p class="text-justify">The concept behind the Logo of Mahatma Gandhi Central University, Bihar (MGCUB) is derived from the Charkha, an iconic symbol representing Mahatma Gandhi. The Charkha is wrapping tricolor yarn spun by its dynamic activities symbolizing our national flag making it a University of national importance. Charka (form of wheel) is a symbolic representation for the progress and growth. The spinning wheel has been used metaphorically to signify transformation of raw material into a productive material that fabricates our society. This represents the national unity, dynamism and overall progress. </p>
                        <p class="text-justify">The color of Charkha is depicted in indigo blue connoting the first public movement against colonial rule by Mahatma Gandhi, the Champaran movement (Indigo Movement) one of the significant event of the region. The foundation year of the University marks centenary of this famous Champaran Movement. The design is an abstraction of the Charkha and the national movement.</p>
                        <p class="text-justify">In addition to the above concepts the design also represents the glowing/shinning sun/star in the middle. Sun is the source of energy connoting to the source of knowledge. Charkha is shown with eight spokes denoting spreading of knowledge and wisdom in all eight directions adding to the slogan of the University. In summary, the Logo features the concept of: </p>

                        <ul class="custom-list-style mb-20 text-justify">
                            <li><i class="fa fa-check"></i> Charkha - (Symbolism of Gandhiji and wheel of life that generates yarn to weave our society besides national movement)</li>
                            <li><i class="fa fa-check"></i> Sun - (the primary source of all life forms) </li>
                            <li><i class="fa fa-check"></i> Champaran Movement - (specific event that illustrates the first Satyagraha of Gandhiji against the oppressive colonial regime and also signifies the local cultural context)</li>
                            <li><i class="fa fa-check"></i> Shining Star and white inward arrows - in Charkha pointing towards the Centre (people coming together from every direction and converting into shining stars through quality education)</li>
                            <li><i class="fa fa-check"></i> Cardinal Directions - (Spreading knowledge in all directions)</li>
                            <li><i class="fa fa-check"></i> Self Determination & Self-Reliance </li>
                        </ul>

                        <h4 class="inner-title">Symbolism of eight spokes of Charkha wheel</h4>
                        <p class="text-justify">Eight is a very important number in Indian tradition. It is believed that there is a group of eight deities, Asta Dikpala <img src="{{asset('web/images/hindi-txt.jpg')}}" alt="hindi text" width="78" height="16" /> called literally meaning guardians of eight directions.  Asthang Yog (Eightfold yoga) of Patanjali, Sashtang Namaskar (best salutation with whole body), Asht dhatu (considered sacred alloy), Asth prahar (eight divisions of day), ashtkoniya sthapatya (octagonal architecture) are other examples. In Hindu mythology, eight is the number of Adityas, Vasus and forms of Goddess Lakshmi.</p>
                        <p class="text-justify">The Dharmacakra, a Buddhist symbol, has eight spokes. Buddha's principal teaching - the Four Noble Truths - ramifies as the Noble Eightfold Path and Buddha has emphasized upon the importance of the eight attainments. </p>
                        <p class="text-justify">Eight also finds a prominent place in <b>Islam</b>, <b>Christianity</b>, <b>Judaism</b>, <b>Sikhism</b> and <b>Jainism.</b></p>
                        <p class="text-justify"><b>In Islam</b>, eight is the number of angels carrying the throne of Allah in heaven, and the number of gates of bahisht (heaven).</p>
