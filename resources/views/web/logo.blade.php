                        <p class="text-justify">"Which sometime were disobedient, when once the longsuffering of God waited in the days of Noah, while the ark was a preparing, wherein few, that is, eight souls were saved by water." (Bible, King James Version, 1. Peter: 20)</p>
                        <p class="text-justify">The Beatitudes are 8 blessings recounted in the Sermon on the Mount in the Gospel of Matthew. (Matthew 5:1-12)</p>
                        <p class="text-justify">Sukhmani Sahib is part of Shri Granth Sahib and was compiled by the fifth Sikh Guru, Guru Shri Arjan Dev Ji. It has exactly 192 hymns with 24 (divisible by 8) sections. Group of 8 hymns are known as Ashtpadi in Sikhism. Ashtpadi, literally 8 steps is a music composition with 8 lines. It is considered rhyme of love and supreme devotion.</p>
