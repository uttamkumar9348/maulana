                                <h4 class="inner-title">Introduction</h4>
                                <p class="text-justify">Ek Bharat Shreshtha Bharat programme aims to enhance interaction & promote mutual understanding between people of different states/UTs through the concept of state/UT pairing. The states carry out activities to promote a sustained and structured cultural connect in the areas of language learning, culture, traditions & music, tourism & cuisine, sports and sharing of best practices, etc.</p>

                                <h4 class="inner-title">Background and Need</h4>
                                <p class="text-justify">India is a unique nation, whose fabric has been woven by diverse linguistic, cultural and religious threads, held together into a composite national identity by a rich history of cultural evolution, coupled with a freedom struggle that was built around the tenets of non-violence and justice. The spirit of mutual understanding amidst a shared history has enabled a special unity in diversity, which stands out as a tall flame of nationhood that needs to be nourished and cherished into the future.</p>
                                <p class="text-justify">Time and technology have narrowed down distances in terms of connect and communication. In an era that facilitates mobility and outreach, it is important to establish cultural exchanges between people of different regions, as a means to further human bonding and a common approach to nation-building. Mutual understanding and trust are the foundations of India's strength and all citizens should feel culturally integrated in all corners of India. Students from the north-east, for example, should not feel like 'strangers in a strange land' when they arrive in Delhi, or a person from Uttarakhand should not feel like an outsider in Kerala.</p>

                                <h4 class="inner-title">Concept of EBSB</h4>
                                <p class="text-justify">The idea of a sustained and structured cultural connect between people of different regions was mooted by Prime Minister
                                    Shri Narendra Modi during the Rashtriya Ekta Divas held on 31st October, 2015, to commemorate the birth anniversary
                                    of Sardar Vallabhbhai Patel. Hon’ble Prime Minister propounded that cultural diversity is a joy that ought to be
                                    celebrated through mutual interaction &amp; reciprocity between people of different States and UTs so that a common
                                    spirit of understanding resonates throughout the country. Every State and UT in the country would be paired with
                                    another State/UT for a time period, during which they would carry out a structured engagement with one another in
                                    the spheres of language, literature, cuisine, festivals, cultural events, tourism etc. For example, Andhra Pradesh
                                    is paired with Punjab for During this period, Punjabis would attempt to learn key words in Telugu, a few Telugu
                                    books would be translated into Punjabi &amp; vice-versa, Andhraites would hold food festivals offering Punjabi dishes,
                                    Punjabis would perform Andhra folk dances, while Andhraites would perform Bhangra at staged events etc. This pattern
                                    of cultural adoption of the partner State/UT would be followed by all states and UTs.
                                </p>
                                <h4 class="inner-title">How EBSB Has been taken ahead</h4>
                                <p class="text-justify">States/UTs in India have been paired with each other for a fixed time period. The paired States/UTs signed MoUs with
                                    each other, delineating a set of activities that they would carry out. An activity calendar for each pair was prepared
                                    through mutual consultation, paving the way for a systematic process of mutual engagement. Such interaction between
                                    different segments of the population of each pair of States /UTs at the cultural level, generated the vibrance of
                                    understanding &amp; appreciation amongst the people and forged mutual bonding, thus securing an enriched value system
                                    of unity in the nation. The pairing of States/ UTs is available on the website.</p>
